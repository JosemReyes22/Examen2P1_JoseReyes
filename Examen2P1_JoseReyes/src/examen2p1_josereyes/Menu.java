    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Gusanito
        JOptionPane.showMessageDialog(null,"Usted a ingresado al ejercicio de Gusanito Hambiento");
        System.out.print("Ingrese las dimensiones del tablero n: ");
        int n = scanner.nextInt();
        System.out.print("Ingrese la dimension del tablero m: ");
        int m = scanner.nextInt();
        scanner.nextLine();
        Gusanito gusanito = new Gusanito(n, m);
        System.out.println("Ingrese las instrucciones (Ejemplo: 3RT, 2DN, 4LT,).");
        System.out.println("UP(arriba), DN(abajo), RT(derecha), LT(izquierda)");
        gusanito.imprimirTablero();
        System.out.println("Para terminar, ingrese 'FIN':");
        String instruccion = scanner.nextLine();
        while (!instruccion.equals("FIN")) {
            gusanito.addInstruccion(instruccion);
            instruccion = scanner.nextLine();
        }
        System.out.println("Â¡Comenzando el juego!");
        gusanito.imprimirTablero();
        gusanito.mover();
    }//GEN-LAST:event_jButton2ActionPerformed

